

class Assisgment{

	public static void main(String[] args){

	int c = 10;
        System.out.println("Assignment Operators:");
        c += 5; 

        System.out.println("c += 5: " + c);
        c -= 3; 

        System.out.println("c -= 3: " + c);

        c *= 2; 
        System.out.println("c *= 2: " + c);
        c /= 4; 

        System.out.println("c /= 4: " + c);
        c %= 2; // equivalent to c = c % 2;

        System.out.println("c %= 2: " + c);
        System.out.println();

}

}